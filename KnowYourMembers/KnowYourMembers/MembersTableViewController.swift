    func getMembers() {
        var query = PFQuery(className:"Members")
        query.findObjectsInBackgroundWithBlock {
            (objects: [PFObject]?, error: NSError?) -> Void in
            
            if error == nil {
                // The find succeeded.
                print("Successfully retrieved \(objects!.count) scores.")
                // Do something with the found objects
                if let objects = objects! as? [PFObject] {
                    for object in objects {
                        self.firstNames.append(object["firstName"] as! String)
                        self.lastNames.append(object["lastName"] as! String)
                        self.funFactOnes.append(object["funFact1"] as! String)
                        self.funFactTwos.append(object["funFact2"] as! String)
                    }
                }
                self.tableView.reloadData()
            } else {
                // Log details of the failure
                print("Error: \(error!) \(error!.userInfo)")
            }
        }
    }

